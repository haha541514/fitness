package com.fitness.vo;

public class NewsForm {

	private String news_title;
	private String nttype;
	private String ntcontent;
	private String newscPhone;
	private String newscHotline;
	
	
	
	public String getNews_title() {
		return news_title;
	}
	public void setNews_title(String news_title) {
		this.news_title = news_title;
	}
	public String getNttype() {
		return nttype;
	}
	public void setNttype(String nttype) {
		this.nttype = nttype;
	}
	public String getNtcontent() {
		return ntcontent;
	}
	public void setNtcontent(String ntcontent) {
		this.ntcontent = ntcontent;
	}
	public String getNewscPhone() {
		return newscPhone;
	}
	public void setNewscPhone(String newscPhone) {
		this.newscPhone = newscPhone;
	}
	public String getNewscHotline() {
		return newscHotline;
	}
	public void setNewscHotline(String newscHotline) {
		this.newscHotline = newscHotline;
	}
	
	
	
	
	
	
}
